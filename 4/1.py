#Написати функцію < square > , яка прийматиме один аргумент - сторону квадрата, і вертатиме 3 значення (кортеж): периметр квадрата, площа квадрата та його діагональ.
a = int(input("Введіть довжину: "))

import math

def square(a):
    p = 4*a
    s = a**2
    c = math.sqrt(a**2 + a**2)
    return p, s, c

print (square(a))
